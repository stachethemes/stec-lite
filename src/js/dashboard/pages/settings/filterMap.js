export const getTabKeywords = (tab) => {

    const unique = [
        tab,
        ...new Set(
            filterMap[tab].flatMap(item => getSectionKeywords(tab, item.section))
        )
    ];

    return unique;
}

export const getSectionKeywords = (tab, section) => {

    const obj = filterMap[tab].find(item => item.section === section);
    const keywords = obj ? [...new Set(Object.values(obj.props).flat())] : [];

    return [
        tab,
        section,
        ...keywords
    ];

}

export const getPropsKeywords = (tab, section, prop) => {
    const keywords = filterMap[tab].find(item => item.section === section)?.props[prop] ?? [];

    return [
        tab,
        section,
        ...keywords
    ];
}